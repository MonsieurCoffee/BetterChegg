package com.netlab.betterchegg.models;

public class ProfileResponse {
    private int id;
    private String username, email, password;

    public ProfileResponse(int id, String username, String email, String password) {
        this.id = id;
        this.username = username;
        this.email = email;
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
